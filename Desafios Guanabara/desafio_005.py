# Desafio - Faça umm programa que leia um número inteiro
# e mostre na tela o seu sucessor e seu antecessor

numero = int(input('Digite um número: '))

print(f'O antecessor do número {numero} é {numero-1} e o seu sucessor é {numero+1}!')

# Usando esta forma de print economizo memória, pois há menos variáveis no código.